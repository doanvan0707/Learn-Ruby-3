# Ruby Arithmetic Operators

a = 10
b = 5

# Addition
puts "Addtion: #{a + b}" # 15

# Subtraction
puts "Subtraction: #{a - b}" # 5

# Multiplication
puts "Multiplication: #{a * b}" # 50

# Division
puts "Division: #{a / b}" # 2

# Modulus
puts "Modulus: #{a % b}" # 0

# Exponent
puts "Exponent: #{a ** b}" # 100000
